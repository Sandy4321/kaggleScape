# ## Why creating another kernel then?
# 
# I found Heng CherKeng kind of an experimental playground and I wanted to have a clear and straightforward code written for Pytorch that I can refer to later in time.
# The goal of this kernel is not to burst public/private lb score but rather to understand how Unets works using Pytorch.
# It has a clear and well documented code and takes the learning path approach. Of course I'll add more and more improvements to it to eventually reach the top results (on 31-08-2017 it reaches 0.987 on public LB). Anyone who's willing to help is welcome to open PRs on the github repo. I engage myself to review and merge them.
